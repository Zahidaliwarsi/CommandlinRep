package SerializationPackage;

public class Kids {
	String kidname;
	int kidage;
	public Kids(String kidname, int kidage) {
		super();
		this.kidname = kidname;
		this.kidage = kidage;
	}
	public String getKidname() {
		return kidname;
	}
	public void setKidname(String kidname) {
		this.kidname = kidname;
	}
	public int getKidage() {
		return kidage;
	}
	public void setKidage(int kidage) {
		this.kidage = kidage;
	}
	

}
